package com.jointown.zy.web.filter;

import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.commons.lang.StringUtils;
import org.apache.shiro.SecurityUtils;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.web.servlet.HandlerInterceptor;
import org.springframework.web.servlet.ModelAndView;

public class UrlFilter implements HandlerInterceptor {

	private static final Logger Log = LoggerFactory.getLogger(UrlFilter.class);
	 /** 
     * 在业务处理器处理请求之前被调用 
     * 如果返回false 
     *     从当前的拦截器往回执行所有拦截器的afterCompletion(),再退出拦截器链 
     *  
     * 如果返回true 
     *    执行下一个拦截器,直到所有的拦截器都执行完毕 
     *    再执行被拦截的Controller 
     *    然后进入拦截器链, 
     *    从最后一个拦截器往回执行所有的postHandle() 
     *    接着再从最后一个拦截器往回执行所有的afterCompletion() 
     */  
	@Override
	public boolean preHandle(HttpServletRequest request,
			HttpServletResponse response, Object handler) throws Exception {
		// TODO Auto-generated method stub
		Log.info("==============执行顺序: 1、preHandle================");  
		String code = request.getParameter("golableMenuPath");
		if(StringUtils.isNotEmpty(code)){
			String[] codes = code.split("/");
			SecurityUtils.getSubject().getSession().setAttribute("FirstMenu", codes[0]);
			SecurityUtils.getSubject().getSession().setAttribute("SecondMenu", codes[0]+"/"+codes[1]);
			SecurityUtils.getSubject().getSession().setAttribute("ThirdMenu", code);
			/*request.setAttribute("FirstMenu", codes[0]);
			request.setAttribute("SecondMenu", codes[0]+"/"+codes[1]);
			request.setAttribute("ThirdMenu", code);*/
		}
		return true;
	}

	//在业务处理器处理请求执行完成后,生成视图之前执行的动作 
	@Override
	public void postHandle(HttpServletRequest request,
			HttpServletResponse response, Object handler,
			ModelAndView modelAndView) throws Exception {
		// TODO Auto-generated method stub
		
	}

	/** 
     * 在DispatcherServlet完全处理完请求后被调用  
     *  
     *   当有拦截器抛出异常时,会从当前拦截器往回执行所有的拦截器的afterCompletion() 
     */  
	@Override
	public void afterCompletion(HttpServletRequest request,
			HttpServletResponse response, Object handler, Exception ex)
			throws Exception {
		// TODO Auto-generated method stub
		
	}

}
